import { ToastContainer, toast } from 'react-toastify';

export const showAlertWarning = (text, mode) => {
  toast.warn(text, {
    position: 'top-right',
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: 'light',
  });
};

